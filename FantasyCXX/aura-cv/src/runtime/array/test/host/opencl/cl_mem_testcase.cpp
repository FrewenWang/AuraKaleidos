    cl::Event cl_event;

    Sizes3 sizes = Sizes3(height * depth, width, cn);
    Sizes strides = Sizes(0, sizes.m_width * ElemTypeSize(ElemType::U16) * cn);

    strides.m_width = AURA_ALIGN(strides.m_width, cl_rt->GetCLLengthAlignSize());

    cl_iaura_format fmt;
    fmt.iaura_channel_order     = CL_R;
    fmt.iaura_channel_data_type = CLMem::GetCLIauraChannelDataType(ElemType::U16);

    size_t iaura_width = CLMem::GetCLIauraWidth(sizes.m_width * sizes.m_channel, fmt.iaura_channel_order);
    strides.m_height   = AURA_ALIGN(height, cl_rt->GetCLSliceAlignSize(fmt, iaura_width, height) / cl_rt->GetCLLengthAlignSize()) * depth;

