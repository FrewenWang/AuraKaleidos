    DT_S32 mem_types[] = {AURA_MEM_HEAP, AURA_MEM_DMA_BUF_HEAP};

    std::shared_ptr<CLRuntime> cl_rt = ctx->GetCLEngine()->GetCLRuntime();
    CLKernel cl_kernel(ctx, "Iaura3DTest", "iaura3d_add", {cl_iaura3d_str}, "");

